</table>
<table align="center" width="60%">
    <tr>
        <td><p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The selling process of real estate property in the Philippines could be a nightmare at first glance. Considering the voluminous paper work required from owners and the numerous trips to government offices you would have endure, it could be intimidating. However, you just need to familiarize yourself with the procedures and the legal documents you must need to prepare in order to reap the benefits of investing in real estate.</p></td>
    </tr>
    <tr>
        <td align="center"><br><h3>Allow us to guide you with a step by step means of successfully selling and transferring ownership of real estate properties in the country.</h3></td>
    </tr>
