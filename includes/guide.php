            <b><img src="includes/images/sign.png" height="50px" width="50px">&nbsp;&nbsp;&nbsp;7. Acceptance of Owner</b>
        </td>
    </tr>
    <tr>
        <td style="padding-left: 10px; padding-right: 10px">
            <p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Once the owner has signed the Letter of Intent, this will signify the acceptance of the terms given by the buyer. The seller is bound to promise not to offer the property to other buyers as long as the purchaser does not breach the conditions provided in the letter.</p>
        </td>
    </tr>
</table>
