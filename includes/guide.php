            <b><img src="includes/images/sign.png" height="50px" width="50px">&nbsp;&nbsp;&nbsp;1. Owner and Broker will Sign a Contract of Agreement </b>
        </td>
    </tr>
    <tr>
        <td style="padding-left: 10px; padding-right: 10px">
            <p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The seller and real estate agent will discuss and agree on the terms of the sale, the professional fee or the commission. At this stage, the broker must also scrutinize the existing documents of the owner in order to check the status of the property. The scrutiny of the documents entails the condition of the land title if it is free from loans, liens and encumbrances.</p>
        </td>
    </tr>
</table>
