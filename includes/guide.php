        <td>
            <b><img src="includes/images/sell.png" height="50px" width="50px">&nbsp;&nbsp;&nbsp;2. Owner should issue an Authority to Sell</b>
        </td>
    </tr>
    <tr>
        <td style="padding-left: 10px; padding-right: 10px">
            <p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;This contract will stipulate whether the owner will bestow upon the broker exclusive rights to sell or a non-exclusive authorization to sell his or her property. This agreement will legally bind the broker as an agent of the owner providing for the amount of commission and the pertinent information of the property to be sold. This document will also serve as proof that the broker was legally authorized to represent the owner in selling the property.<br><br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The broker usually get hold of the necessary documents prior to marketing the property to make sure that it is free from encumbrances. An encumbrance means that someone else has a right to, interest in, or legal liability on real property that either prohibits the passing of title to the property or which would diminish its value. The owner must also provide for an authority letter to transact addressed to the selling broker so the latter could get certified true copies of the Title, Tax Declaration and Tax Clearance. </p>
        </td>
    </tr>
</table>
