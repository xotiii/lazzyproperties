        <td style="padding-left: 10px; padding-right: 10px">
            <p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;After having the deed of sale signed by both parties, the document must be notarized to protect the document from fraud. Once a document has been notarized, it will become a public document and would be binding to third persons. This means that if anyone wishes to prove a right of ownership on the property, he or she must be able to prove that they have a right even before the execution of such document. Notarization will protect the buyer from fraud and possible unfounded claims by third persons in the future.</p>
