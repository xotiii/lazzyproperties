            <b><img src="includes/images/mail.png" height="50px" width="50px">&nbsp;&nbsp;&nbsp;6. Letter of Intent or Offer to Buy</b>
        </td>
    </tr>
    <tr>
        <td style="padding-left: 10px; padding-right: 10px">
            <p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;A Letter of Intent is a legal writing offered by the buyer to the owner of the property to declare his or her intention to purchase. This is usually set the first stage in documenting a sale of real property. The letter of intent is a non-binding document which provides for the key transaction agreements such as the price, amount of deposit, length of due diligence period (a stipulated time for a buyer to investigate the property to ensure that they are satisfied before finalizing the purchase) , treatment of financing (period of amortization or loan) and other pertinent terms.</p>
        </td>
    </tr>
</table>
