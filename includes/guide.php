        </td>
    </tr>
    <tr>
        <td style="padding-left: 10px; padding-right: 10px">
            <p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;This is the amount of money given by the home buyer while seeking or applying for financing in order to pay the remaining balance. The earnest money is provided to hold the property subject to the due diligence of the buyer and can be forfeited once there is default on his or her part. The money can also be refundable subject to deductions depending on the agreement between the parties.</p>
        </td>
    </tr>
</table>
