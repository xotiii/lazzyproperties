            <p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The broker must be able to check the property in order to make an assessment of its current market value. The appraisal of the market value of the property is necessary in determining the actual current price of the property. Many factors must be taken into account in determining the value of the asset such as the location and area.</p>
        </td>
    </tr>
</table>
