    </tr>
    <tr>
        <td style="padding-left: 10px; padding-right: 10px">
            <p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The owner and the broker must agree on how to market the property, limitations such as privacy when it comes to releasing photos and disclosing the location of the property online must be considered. Moreover, both parties must decide on how to split the marketing costs such as the transportation and communication expenses. Although, the common practice nowadays is that the broker will shoulder the expenses depending on the amount of his or her commission.</p>
