                                                        <a style="font-size: 13px; color: black;">Property giant Ayala Land Inc. (ALI) has lost ownership of a prime 6.8-hectare lot in Las Piñas City, which it had converted into a golf course and materials depot.
In a 30-page ruling, the Supreme Court (SC) nullified ALI’s right to possess the property as it declared petitioners Yu Hwa Ping and Mary Gaw, and the heirs of Andres Diaz and Josefa Mia, as the rightful owners of the disputed land.The landmark decision, penned by then SC Justice Jose Mendoza, recognized that irregularities could accompany the registration of land and must be nullified to protect the public and the Torrens system.The high court’s Second Division voided ALI’s land titles for the property, which was located inside Ayala Southvale Subdivision, after concluding that the land surveys made to register it nearly 90 years ago were invalid. “The court cannot close its eyes to the blatant defects on the surveys upon which the original titles of ALI were derived simply because its titles were registered,” the tribunal said in its July 26 decision, which was made public only recently.




